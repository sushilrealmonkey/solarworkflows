function StaffFormModal({
  title,
  values,
  setValues,
  roles,
  errors,
  onClose,
  onSubmit,
  saving,
  showStatus,
  submitLabel,
}: {
  title: string;
  values: StaffFormValues;
  setValues: (values: StaffFormValues) => void;
  roles: SettingsRole[];
  errors: Record<string, string>;
  onClose: () => void;
  onSubmit: (event: FormEvent<HTMLFormElement>) => void;
  saving: boolean;
  showStatus: boolean;
  submitLabel: string;
}) {
  const update = (key: keyof StaffFormValues, value: string) =>
    setValues({ ...values, [key]: value });

  return (
    <Modal
      title={title}
      onClose={onClose}
      onSubmit={onSubmit}
      submitLabel={submitLabel}
      submitting={saving}
    >
      <TextInput
        label="Full Name"
        value={values.full_name}
        onChange={(value) => update("full_name", value)}
        error={errors.full_name}
        required
      />
      <TextInput
        label="Phone"
        value={values.phone}
        onChange={(value) => update("phone", value)}
        error={errors.phone}
      />
      <TextInput
        label="Email"
        type="email"
        value={values.email}
        onChange={(value) => update("email", value)}
        error={errors.email}
        required
      />
      <SelectInput
        label="Role"
        value={values.role_id}
        onChange={(value) => update("role_id", value)}
        options={[
          { value: "", label: "No role" },
          ...roles.map((role) => ({
            value: role.id,
            label: role.role_name,
          })),
        ]}
      />
      {showStatus ? (
        <SelectInput
          label="Status"
          value={values.status}
          onChange={(value) => update("status", value)}
          options={staffStatusOptions.map((status) => ({
            value: status,
            label: labelize(status),
          }))}
        />
      ) : null}
    </Modal>
  );
}

function StaffStatusBadge({ value }: { value: string | null | undefined }) {
  const tone =
    value === "active" ? "green" : value === "inactive" ? "red" : "amber";

  return <Badge tone={tone}>{labelize(value)}</Badge>;
}

function StaffRowActions({
  isMenuOpen,
  lastLogin,
  member,
  onEdit,
  onMenuOpenChange,
  onStatusAction,
}: {
  isMenuOpen: boolean;
  lastLogin: string;
  member: SettingsStaff;
  onEdit: () => void;
  onMenuOpenChange: (isOpen: boolean) => void;
  onStatusAction: () => void;
}) {
  const statusActionLabel =
    member.status === "active" ? "Deactivate" : "Activate";

  return (
    <div
      className="relative ml-auto flex w-fit items-center justify-end gap-1.5"
      onBlur={(event) => {
        if (!event.currentTarget.contains(event.relatedTarget)) {
          onMenuOpenChange(false);
        }
      }}
    >
      <button
        aria-label={`Edit ${member.full_name ?? "staff member"}`}
        className="inline-flex h-9 w-9 items-center justify-center rounded-lg border border-stone-200 bg-white text-slate-700 shadow-sm transition hover:bg-orange-50 hover:text-[#06173f] focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-orange-500"
        onClick={onEdit}
        type="button"
      >
        <PencilIcon />
      </button>
      <button
        aria-expanded={isMenuOpen}
        aria-haspopup="menu"
        aria-label={`More actions for ${member.full_name ?? "staff member"}`}
        className="inline-flex h-9 w-9 items-center justify-center rounded-lg border border-stone-200 bg-white text-slate-700 shadow-sm transition hover:bg-orange-50 hover:text-[#06173f] focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-orange-500"
        onClick={() => onMenuOpenChange(!isMenuOpen)}
        type="button"
      >
        <VerticalDotsIcon />
      </button>

      {isMenuOpen ? (
        <div
          aria-label="Staff actions"
          className="absolute right-0 top-[calc(100%+0.45rem)] z-30 w-56 rounded-lg border border-stone-200 bg-white p-1.5 text-left shadow-xl"
          role="menu"
        >
          <div className="rounded-md px-3 py-2">
            <p className="text-xs font-semibold uppercase tracking-wide text-slate-500">
              Last login
            </p>
            <p className="mt-1 text-sm font-medium text-slate-900">
              {lastLogin}
            </p>
          </div>
          <button
            className={`mt-1 flex w-full items-center rounded-md px-3 py-2 text-left text-sm font-semibold transition hover:bg-orange-50 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-inset focus-visible:ring-orange-500 ${
              member.status === "active" ? "text-rose-700" : "text-[#06173f]"
            }`}
            onClick={onStatusAction}
            role="menuitem"
            type="button"
          >
            {statusActionLabel}
          </button>
        </div>
      ) : null}
    </div>
  );
}

